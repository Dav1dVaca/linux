# uname -a: muestra información completa del sistema operativo y el kernel
uname -a

# lscpu: muestra información detallada del procesador (arquitectura, núcleos, hilos, etc.)
lscpu

# free -h: muestra la memoria RAM disponible y usada; -h la muestra en formato legible (MB/GB)
free -h

# df -h: muestra el uso del disco duro; -h lo presenta en formato legible (MB/GB)
df -h