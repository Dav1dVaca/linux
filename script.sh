
# echo: muestra un mensaje en pantalla o lo envía a un archivo si se usa con >
echo "Hola Linux" > archivo.txt     # > redirige la salida del echo al archivo; si no existe lo crea y si existe lo sobrescribe

# cat: muestra el contenido de un archivo en la terminal
cat archivo.txt                      # muestra el contenido de archivo.txt

# cp: copia un archivo a otro nombre o ubicación
cp archivo.txt copia.txt             # crea una copia llamada copia.txt a partir de archivo.txt

# ls: lista archivos y carpetas del directorio actual
ls -l                                # -l muestra la lista en formato largo (permisos, tamaño, fechas)

# rm: elimina archivos permanentemente
rm archivo.txt                       # borra el archivo archivo.txt del sistema

